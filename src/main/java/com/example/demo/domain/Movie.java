package com.example.demo.domain;

import com.baomidou.mybatisplus.annotations.TableField;
import com.baomidou.mybatisplus.annotations.TableId;
import com.baomidou.mybatisplus.annotations.TableName;
import com.baomidou.mybatisplus.enums.IdType;

import java.util.Date;

/**
 * Created by Summer on 2018/3/21.
 */
@TableName("movie")
public class Movie {
    @TableId(value = "movie_id",type= IdType.AUTO)
    private Integer id;

    @TableField("movie_name")
    private String name;

    @TableField("movie_director")
    private String director;

    @TableField("movie_actor")
    private String actor;

    @TableField("movie_language")
    private String language;

    @TableField("movie_type")
    private String type;

    @TableField("movie_area")
    private String area;

    @TableField("movie_date")
    private Date date;

    @TableField("movie_imageurl")
    private String imgUrl;

    @TableField("movie_score")
    private String score;

    @TableField("movie_url")
    private String url;

    public Integer getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDirector() {
        return director;
    }

    public void setDirector(String director) {
        this.director = director;
    }

    public String getActor() {
        return actor;
    }

    public void setActor(String actor) {
        this.actor = actor;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
